# string data type
data_str = "Universitas Harkat Negeri"
name = "Raditya Syafi W"

# integer data type
age = 19

# float data type
height = 1.80
pi = 3.14

# boolean data type
is_student = True

# printing data types
print("data_str:", data_str, "bertipe", type(data_str))
print("name:", name, "bertipe", type(name))
print("age:", age, "bertipe", type(age))
print("height:", height, "bertipe", type(height))
print("pi:", pi, "bertipe", type(pi))
print("is_student:", is_student, "bertipe", type(is_student))

# case_sensitive
a = 5 >= 3
A = 5 < 3
print(a)
print(A)

